const STEP_REM = 0.1;
const DEFAULT_STEP = 0;
const MIN_STEP = -1;
const MAX_STEP = 7;
const STORAGE_KEY = 'kesson-font-step';
const MIGRATION_KEY = 'kesson-font-step-v5';

export const FONT_STEP_RANGE = {
  stepRem: STEP_REM,
  defaultStep: DEFAULT_STEP,
  min: MIN_STEP,
  max: MAX_STEP,
};

export const FONT_STEP_CHANGE_EVENT = 'kesson:font-step-change';

// Global UI font sizes
const FONT_VARS = {
  '--kesson-font-size-ui-xs': 0.85,
  '--kesson-font-size-ui-sm': 0.88,
};

// Component base values (rem)
const CLASS_VARS = {
  // -- Card & section --
  '--kesson-section-heading': 0.88,
  '--kesson-card-title': 1.00,
  '--kesson-card-text': 0.92,
  '--kesson-card-summary': 0.92,

  // -- Overlay & guide --
  '--kesson-overlay-tagline': 0.92,
  '--kesson-overlay-tagline-en': 0.88,
  '--kesson-control-guide': 0.82,
  '--kesson-surface-btn': 0.88,

  // -- Footer --
  '--kesson-footer-line': 0.82,
  '--kesson-footer-signature-size': 0.88,

  // -- Dev HUD --
  '--kesson-dev-hud-font-size': 0.80,

  // -- Topbar --
  '--kesson-topbar-link-size': 0.88,
  '--kesson-topbar-credit-size': 0.84,
  '--kesson-topbar-note-size': 0.88,
  '--kesson-topbar-meta-size': 0.86,
  '--kesson-topbar-meta-author-size': 0.84,
  '--kesson-topbar-subtitle-size-md': 0.88,
  '--kesson-topbar-title-size': 0.88,
  '--kesson-topbar-main-title-size': 1.04,
  '--kesson-topbar-main-title-size-sm': 0.92,

  // -- Graphic switcher --
  '--kesson-graphic-switcher-label': 0.88,
  '--kesson-graphic-switcher-btn': 0.88,

  // -- Hero h1 --
  '--kesson-h1-size': 1.10,
};

function normalizeStep(step) {
  const parsed = Number.parseInt(step, 10);
  if (!Number.isFinite(parsed)) return DEFAULT_STEP;
  return Math.min(MAX_STEP, Math.max(MIN_STEP, parsed));
}

function computeRem(base, step) {
  return `${(base + step * STEP_REM).toFixed(2)}rem`;
}

function getControlledValues(step) {
  const values = {};

  for (const [varName, base] of Object.entries(FONT_VARS)) {
    values[varName] = computeRem(base, step);
  }
  for (const [varName, base] of Object.entries(CLASS_VARS)) {
    values[varName] = computeRem(base, step);
  }

  return values;
}

function emitStepChange(step, values) {
  if (typeof window === 'undefined') return;
  window.dispatchEvent(new CustomEvent(FONT_STEP_CHANGE_EVENT, {
    detail: {
      step,
      values,
      cssText: buildFontDefaultsCss(step),
    },
  }));
}

export function initFontSizeCtrl() {
  // CHANGED(2026-03-07): #116 Bug #11 — 旧デフォルト(0)→新デフォルト(3)の一度きり移行
  if (!localStorage.getItem(MIGRATION_KEY)) {
    const old = localStorage.getItem(STORAGE_KEY);
    if (old === '0' || old === null) {
      localStorage.setItem(STORAGE_KEY, String(DEFAULT_STEP));
    }
    localStorage.setItem(MIGRATION_KEY, '1');
  }
  const step = parseInt(localStorage.getItem(STORAGE_KEY) ?? String(DEFAULT_STEP), 10);
  applyStep(step);

  document.getElementById('font-size-down')?.addEventListener('click', () => {
    const cur = getCurrentStep();
    if (cur > MIN_STEP) setStep(cur - 1);
  });
  document.getElementById('font-size-up')?.addEventListener('click', () => {
    const cur = getCurrentStep();
    if (cur < MAX_STEP) setStep(cur + 1);
  });
  document.getElementById('font-size-reset')?.addEventListener('click', () => {
    setStep(DEFAULT_STEP);
  });
}

export function getCurrentStep() {
  return normalizeStep(localStorage.getItem(STORAGE_KEY) ?? String(DEFAULT_STEP));
}

export function setStep(step) {
  const nextStep = normalizeStep(step);
  localStorage.setItem(STORAGE_KEY, String(nextStep));
  applyStep(nextStep);
}

function applyStep(step) {
  const root = document.documentElement;
  const nextStep = normalizeStep(step);
  const values = getControlledValues(nextStep);

  for (const [varName, value] of Object.entries(values)) {
    root.style.setProperty(varName, value);
  }

  const down  = document.getElementById('font-size-down');
  const up    = document.getElementById('font-size-up');
  const reset = document.getElementById('font-size-reset');
  if (down)  down.disabled  = nextStep <= MIN_STEP;
  if (up)    up.disabled    = nextStep >= MAX_STEP;
  if (reset) reset.disabled = nextStep === DEFAULT_STEP;

  emitStepChange(nextStep, values);
}

export function buildFontDefaultsCss(step = getCurrentStep()) {
  const values = getControlledValues(normalizeStep(step));
  return [
    '/* font-size defaults (generated by dev panel) */',
    ...Object.entries(values).map(([varName, value]) => `${varName}: ${value};`),
  ].join('\n');
}
